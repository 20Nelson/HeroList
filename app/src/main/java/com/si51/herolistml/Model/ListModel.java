package com.si51.herolistml.Model;

public class ListModel {
    private String hero_name, hero_avatar, hero_role, hero_specially;
    private int hero_id;

    public String getHero_name() { return hero_name; }
    public void setHero_name(String hero_name) { this.hero_name = hero_name; }

    public String getHero_avatar() { return hero_avatar; }
    public void setHero_avatar(String hero_avatar) { this.hero_avatar = hero_avatar; }

    public String getHero_role() { return hero_role; }
    public void setHero_role(String hero_role) { this.hero_role = hero_role; }

    public String getHero_specially() { return hero_specially; }
    public void setHero_specially(String hero_specially) { this.hero_specially = hero_specially; }

    public int getHero_id() { return hero_id; }
    public void setHero_id(int hero_id) { this.hero_id = hero_id;}
}